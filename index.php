<h2>
  Elo Rating System (PHP Implementation)
</h2>
<h4>
  PHP implementation of the rating algorithm that was used in
  <a href='https://en.wikipedia.org/wiki/History_of_Facebook#FaceMash'>Facemash</a>
  by Mark Zuckerberg.
</h4>
<p>It's based on the
  <a href="https://en.wikipedia.org/wiki/Elo_rating_system#Mathematical_details">Elo Rating System</a>
  , usually used for calculating the relative skill levels of players in zero-sum games such as chess.
</p>
<p>
  The initial rating of each player in Facemash was 1400, yet we don't know the K-factor of
  the game. (Info based on the <a href='https://en.wikipedia.org/wiki/The_Social_Network'>Social Network</a> Movie!)
</p>
